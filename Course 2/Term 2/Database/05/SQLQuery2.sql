SELECT Loans.Сумма, Loans.Код_вида
FROM Loans
WHERE Код_клиента = (SELECT TOP(1) Код_клиента
							FROM Loans Lloans 
							WHERE Loans.Код_вида = Lloans.Код_вида
							ORDER BY Сумма DESC)

