SELECT top 1
	(SELECT AVG(Loans.Сумма) 
	 FROM Loans
	 WHERE Loans.Код_вида = 1),
	(SELECT AVG(Loans.Сумма) 
	 FROM Loans
	 WHERE Loans.Код_вида = 2),
	 (SELECT AVG(Loans.Сумма) 
	 FROM Loans
	 WHERE Loans.Код_вида = 3)
FROM Loans
