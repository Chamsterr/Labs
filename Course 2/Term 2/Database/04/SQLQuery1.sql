FROM AUDITORIUM  
JOIN AUDITORIUM_TYPE on AUDITORIUM.AUDITORIUM_TYPE = AUDITORIUM_TYPE.AUDITORIUM_TYPE AND AUDITORIUM_TYPENAME like '%[ך]מלן‏עונ%'

