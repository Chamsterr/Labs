SELECT STUDENT.NAME, PROGRESS.NOTE, PROFESSION.PROFESSION_NAME, FACULTY.FACULTY, PULPIT.PULPIT_NAME, SUBJECT.SUBJECT,
CASE
	WHEN PROGRESS.NOTE = 6 THEN 'רוסע'
	WHEN PROGRESS.NOTE = 7 THEN 'סול'
	WHEN PROGRESS.NOTE = 8 THEN 'גמסול'
END
FROM PROGRESS
JOIN SUBJECT ON PROGRESS.SUBJECT = SUBJECT.SUBJECT
JOIN STUDENT ON PROGRESS.IDSTUDENT = STUDENT.IDSTUDENT
JOIN GROUPS ON STUDENT.IDGROUP = GROUPS.IDGROUP
JOIN FACULTY ON FACULTY.FACULTY = GROUPS.FACULTY
JOIN PROFESSION ON PROFESSION.PROFESSION = GROUPS.PROFESSION
JOIN PULPIT ON FACULTY.FACULTY = PULPIT.FACULTY
WHERE PROGRESS.NOTE BETWEEN 6 AND 8

